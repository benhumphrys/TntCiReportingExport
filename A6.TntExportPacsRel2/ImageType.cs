namespace Tnt.KofaxCapture.A6.TntExportPacsRel
{
    /// <summary>
    /// Represents an image type.
    /// </summary>
    internal sealed class ImageType
    {
        /// <summary>
        /// Gets or set the Description property.
        /// </summary>
        public string Description { get; set; }

        /// <summary>
        /// Gets or sets the FilterName property.
        /// </summary>
        public string FilterName { get; set; }

        /// <summary>
        /// Gets or sets the Method property.
        /// </summary>
        public int Method { get; set; }

        /// <summary>
        /// Gets or sets the MethodName property.
        /// </summary>
        public string MethodName { get; set; }

        /// <summary>
        /// Gets or sets the MultiplePage property.
        /// </summary>
        public bool MultiplePage { get; set; }

        /// <summary>
        /// Gets or sets the Name property.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the Type property.
        /// </summary>
        public int Type { get; set; }
    }
}
